## Pilot: Codex Guardian

Use this block first after verifying `CODEX_RUNTIME_ID` from Multica runtime data. It creates only `Codex Guardian`, with Codex approval set to `on-request`, one concurrent task, and private visibility.

```bash
CONFIRM_CREATE_CODEX_GUARDIAN=yes <LOCAL_WORKBENCH_REPO>/scripts/create-pilot-agent.sh
```

The script refuses to contact Multica unless `CONFIRM_CREATE_CODEX_GUARDIAN=yes` is set at action time and the effective `MULTICA_PROFILE`, `MULTICA_WORKSPACE_ID`, and `CODEX_RUNTIME_ID` exactly match the verified pilot target values below.

Equivalent command shape:

```bash
EXPECTED_MULTICA_PROFILE="desktop-api.multica.ai"
EXPECTED_MULTICA_WORKSPACE_ID="<workspace-id>"
EXPECTED_CODEX_RUNTIME_ID="<codex-runtime-id>"

MULTICA_PROFILE="${MULTICA_PROFILE:-desktop-api.multica.ai}"
: "${MULTICA_WORKSPACE_ID:?Set MULTICA_WORKSPACE_ID}"
: "${CODEX_RUNTIME_ID:?Set CODEX_RUNTIME_ID}"
ROOT="<LOCAL_WORKBENCH_REPO>"

[[ "$MULTICA_PROFILE" == "$EXPECTED_MULTICA_PROFILE" ]]
[[ "$MULTICA_WORKSPACE_ID" == "$EXPECTED_MULTICA_WORKSPACE_ID" ]]
[[ "$CODEX_RUNTIME_ID" == "$EXPECTED_CODEX_RUNTIME_ID" ]]

multica --profile "$MULTICA_PROFILE" --workspace-id "$MULTICA_WORKSPACE_ID" agent create \
  --name "Codex Guardian" \
  --description "High-risk local ops, command approval, rollback planning, and evidence-first verification." \
  --instructions "$(cat "$ROOT/agents/outer/codex-guardian.md")" \
  --runtime-id "$CODEX_RUNTIME_ID" \
  --runtime-config '{"custom_args":["--ask-for-approval","on-request"]}' \
  --max-concurrent-tasks 1 \
  --visibility private \
  --output json
```

Before expanding beyond the pilot:

```bash
multica --profile "$MULTICA_PROFILE" --workspace-id "$MULTICA_WORKSPACE_ID" agent list --output json
```

## Full Private Roster Script

Run only after the Codex Guardian pilot has passed and the live agent list still contains only the preserved `Workbench Max` plus the existing `Codex Guardian` pilot:

```bash
CONFIRM_CREATE_WORKBENCH_ROSTER=yes <LOCAL_WORKBENCH_REPO>/scripts/create-agent-roster.sh
```

The full-roster script re-checks exact profile, workspace, and runtime IDs before any Multica contact. After those guards pass, it reads and validates the current agent list once before any create. If listing or JSON shape validation fails, it exits before mutation. Successfully parsed existing names drive idempotent skips, so it never recreates `Codex Guardian` and never creates `Workbench Max`.
